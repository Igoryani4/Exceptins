package Total_home_project;

public class Person {
    String f_name;
    String l_name;
    String s_name;
    String d_born;
    int phone_number;
    char gender;

    public Person (String l_name, String f_name, String s_name, String d_born, int phone_number, char gender){
        this.f_name = f_name;
        this.l_name = l_name;
        this.s_name = s_name;
        this.d_born = d_born;
        this.phone_number = phone_number;
        this.gender = gender;
    }



}
